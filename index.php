            <p>Fundada em 2004, a Sumicity já nasceu com vocação para inovações técnicas, pesquisas e foco no atendimento personalizado aos clientes. Hoje, atendemos várias cidades dos estados do Rio de Janeiro e Minas Gerais.</p>
